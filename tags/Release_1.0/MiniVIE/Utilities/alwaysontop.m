%%ALWAYSONTOP controls focus behavior of figures.
%
%   ALWAYSONTOP toggles the state of the current figure. If no figures
%       exist, one is created.
%
%   ALWAYSONTOP(H) toggles the state of figure H. H must be a valid figure
%       handle. 
%
%   ALWAYSONTOP(H,TF) sets the state of figure H to boolean value TF.
%
%   S = ALWAYSONTOP(H) returns but does not change the state of the
%   specified
%       figure. If no figure is specified, the current figure is assumed.
%       (Note: when returning state, input TF cannot be specified.)

